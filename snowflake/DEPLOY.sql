-- ═════════════════════════════════════════════════════════════════════════════
-- SECTION 8: MIGRATION LEDGER
-- ═════════════════════════════════════════════════════════════════════════════

MERGE INTO OVERWATCH_SCHEMA_MIGRATION tgt
USING (
  SELECT
    '2026.06.10-consolidated-deploy' AS MIGRATION_VERSION,
    'Consolidated OVERWATCH deployment (RBAC + Mart + DT + Governance)' AS MIGRATION_NAME,
    'snowflake/DEPLOY.sql' AS SOURCE_FILE,
    'Single-file deployment combining RBAC, mart tables, governance, Dynamic Tables, and cost verification.' AS NOTES
) src
ON tgt.MIGRATION_VERSION = src.MIGRATION_VERSION
WHEN NOT MATCHED THEN INSERT (MIGRATION_VERSION, MIGRATION_NAME, SOURCE_FILE, NOTES)
VALUES (src.MIGRATION_VERSION, src.MIGRATION_NAME, src.SOURCE_FILE, src.NOTES);

