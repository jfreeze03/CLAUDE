-- ═══════════════════════════════════════════════════════════════════════════════
-- OVERWATCH V3 — Consolidated Deployment Script
-- ═══════════════════════════════════════════════════════════════════════════════
--
-- This single file deploys the complete OVERWATCH Snowflake infrastructure.
-- Run sections in order. Each section is idempotent (safe to rerun).
--
-- Deployment order:
--   1. RBAC (roles and grants)
--   2. Runtime objects (database, schema, warehouse, resource monitor)
--   3. Configuration tables (settings, company scope, owner directory)
--   4. Operational tables (action queue, alerts, audit, recovery)
--   5. Forward platform governance (futures controls, evidence)
--   6. Cost verification (savings, chargeback)
--   7. Dynamic Tables (optional — replaces task-based refresh)
--   8. Scheduled tasks (alert delivery, morning brief)
--
-- Prerequisites:
--   - ACCOUNTADMIN or SYSADMIN role
--   - Snowflake Enterprise Edition (for Dynamic Tables and Access History)
--
-- ═══════════════════════════════════════════════════════════════════════════════


-- ═════════════════════════════════════════════════════════════════════════════
-- SECTION 1: RBAC
-- ═════════════════════════════════════════════════════════════════════════════

CREATE ROLE IF NOT EXISTS OVERWATCH_APP_ROLE
  COMMENT = 'Runtime role for OVERWATCH Streamlit app. Read monitoring views, write action queue.';
CREATE ROLE IF NOT EXISTS OVERWATCH_ADMIN_ROLE
  COMMENT = 'Admin role for OVERWATCH mart setup, task management, and schema migrations.';
CREATE ROLE IF NOT EXISTS OVERWATCH_READER_ROLE
  COMMENT = 'Read-only role for OVERWATCH dashboard consumers without write access.';

GRANT ROLE OVERWATCH_READER_ROLE TO ROLE OVERWATCH_APP_ROLE;
GRANT ROLE OVERWATCH_APP_ROLE TO ROLE OVERWATCH_ADMIN_ROLE;
GRANT ROLE OVERWATCH_ADMIN_ROLE TO ROLE SYSADMIN;


-- ═════════════════════════════════════════════════════════════════════════════
-- SECTION 2: RUNTIME OBJECTS
-- ═════════════════════════════════════════════════════════════════════════════

CREATE DATABASE IF NOT EXISTS DBA_MAINT_DB;
CREATE SCHEMA IF NOT EXISTS DBA_MAINT_DB.OVERWATCH;

CREATE WAREHOUSE IF NOT EXISTS OVERWATCH_WH
  WAREHOUSE_SIZE = XSMALL
  AUTO_SUSPEND = 60
  AUTO_RESUME = TRUE
  INITIALLY_SUSPENDED = TRUE
  STATEMENT_TIMEOUT_IN_SECONDS = 600
  COMMENT = 'Dedicated warehouse for OVERWATCH Streamlit app runtime.';

CREATE RESOURCE MONITOR IF NOT EXISTS OVERWATCH_WH_RM
  WITH CREDIT_QUOTA = 50
       FREQUENCY = MONTHLY
       START_TIMESTAMP = IMMEDIATELY
       TRIGGERS ON 80 PERCENT DO NOTIFY
                ON 100 PERCENT DO SUSPEND;

ALTER WAREHOUSE IF EXISTS OVERWATCH_WH SET RESOURCE_MONITOR = OVERWATCH_WH_RM;

-- Grants
GRANT USAGE ON DATABASE DBA_MAINT_DB TO ROLE OVERWATCH_APP_ROLE;
GRANT USAGE ON SCHEMA DBA_MAINT_DB.OVERWATCH TO ROLE OVERWATCH_APP_ROLE;
GRANT USAGE ON DATABASE SNOWFLAKE TO ROLE OVERWATCH_APP_ROLE;
GRANT IMPORTED PRIVILEGES ON DATABASE SNOWFLAKE TO ROLE OVERWATCH_APP_ROLE;
GRANT USAGE ON WAREHOUSE OVERWATCH_WH TO ROLE OVERWATCH_APP_ROLE;
GRANT MONITOR ON WAREHOUSE OVERWATCH_WH TO ROLE OVERWATCH_APP_ROLE;
GRANT ALL PRIVILEGES ON SCHEMA DBA_MAINT_DB.OVERWATCH TO ROLE OVERWATCH_ADMIN_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA DBA_MAINT_DB.OVERWATCH TO ROLE OVERWATCH_READER_ROLE;
GRANT SELECT ON ALL VIEWS IN SCHEMA DBA_MAINT_DB.OVERWATCH TO ROLE OVERWATCH_READER_ROLE;
GRANT SELECT ON FUTURE TABLES IN SCHEMA DBA_MAINT_DB.OVERWATCH TO ROLE OVERWATCH_READER_ROLE;
GRANT SELECT ON FUTURE VIEWS IN SCHEMA DBA_MAINT_DB.OVERWATCH TO ROLE OVERWATCH_READER_ROLE;
GRANT USAGE ON DATABASE DBA_MAINT_DB TO ROLE OVERWATCH_READER_ROLE;
GRANT USAGE ON SCHEMA DBA_MAINT_DB.OVERWATCH TO ROLE OVERWATCH_READER_ROLE;
GRANT USAGE ON WAREHOUSE OVERWATCH_WH TO ROLE OVERWATCH_READER_ROLE;

USE DATABASE DBA_MAINT_DB;
USE SCHEMA OVERWATCH;


